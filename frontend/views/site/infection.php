    <div>В поле зрения гинеколога попадают следующие инфекционные заболевания: <strong>кандидоз (молочница),
            бактериальный вагиноз (гарднереллез), трихомониаз, гонорея, микоплазмоз, хламидиоз, уреаплазмоз, генитальный
