        <ol>
            <li>Схема лечения составляется индивидуально, в зависимости от степени тяжести заболевания и от количества
                сопутствующих заболеваний.
            </li>
            <li>Если вы задумали заняться самолечением, подумайте о том, что все эти инфекционные заболевания лечатся,
